func fatalOnError(err error) {
	if err != nil {
		log.Fatalf("Fatal error: %s\n", err.Error())
	}
}

func main() {
	log.Printf("go-ogle:>\n")
}

func init() {
	log.SetPrefix("")
	log.SetFlags(0)
}

